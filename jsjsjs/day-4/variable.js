var fruits = ['apple', "banana", "orange"]

fruits.unshift("grape")

console.log(fruits)

fruits.push('cherry')

console.log(fruits)

// remove the last element of the array
fruits.pop()
console.log(fruits)

// remove the first element of the array
fruits.shift()
console.log(fruits)



